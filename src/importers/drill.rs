    fn parses_metric_plated_drill_hits() {
        let text = r#"
;TYPE=PLATED
;Layer: PTH_Through
M48
METRIC,LZ,0000.00000
T01C0.30500
T02C1.00000
%
G05
G90
T01
X29.3Y-8.64001
T02
X1000000Y-2000000
M30
"#;
        let parsed = parse_excellon_drill(text, Path::new("fixture.drl")).unwrap();
        assert_eq!(parsed.plating, DrillPlating::Plated);
        assert_eq!(parsed.layer, "PTH_Through");
        assert_eq!(parsed.tools.len(), 2);
        assert_eq!(parsed.hits.len(), 2);
        assert_eq!(parsed.hits[0].at.x_mm, 29.3);
        assert_eq!(parsed.hits[0].drill_mm, 0.305);
        assert_eq!(parsed.hits[1].at.x_mm, 10.0);
        assert_eq!(parsed.hits[1].at.y_mm, -20.0);
    }

